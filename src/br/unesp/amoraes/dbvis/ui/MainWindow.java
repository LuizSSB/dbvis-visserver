    class OnCloseWindowListener extends WindowAdapter {
        public void windowClosing(WindowEvent e) {
            int opt = JOptionPane.showConfirmDialog(e.getWindow(), FunctionsHelper.getString("MainWindow.message.reallyQuit", null), FunctionsHelper.getString("MainWindow.message.quit", null), JOptionPane.YES_NO_OPTION);
            if (opt == JOptionPane.YES_OPTION)
                APIServer.getInstance().stop();
            System.exit(0);
        }
    }
    
