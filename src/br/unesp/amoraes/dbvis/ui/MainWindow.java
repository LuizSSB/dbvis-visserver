    private void setDefaultPanelsLayout() {
        if(isEditing){
            FunctionsHelper.logOnConsole(FunctionsHelper.getString("MainWindow.message.redefinePanelLayout"));
            dataSelectIFrame.setBounds(0,0,new Double(jDesktopPaneMain.getWidth()*0.7).intValue(), new Double(jDesktopPaneMain.getHeight()*0.8).intValue());
            paletteIFrame.setBounds(new Double(jDesktopPaneMain.getWidth()*0.7).intValue(), 0, new Double(jDesktopPaneMain.getWidth()*0.3).intValue(), new Double(jDesktopPaneMain.getHeight()).intValue());
            ConsoleIFrame.getInstance().setBounds(0, new Double(jDesktopPaneMain.getHeight()*0.8).intValue(), new Double(jDesktopPaneMain.getWidth()*0.7).intValue(), new Double(jDesktopPaneMain.getHeight()*0.2).intValue());
            
        }
    }
    
    private void newProject(ProjectType type){
        if(isEditing){
            
        }else{           
            ConsoleIFrame consoleIFrame = ConsoleIFrame.getInstance();
            jDesktopPaneMain.add(consoleIFrame);
            consoleIFrame.setVisible(true);
            
            if(type.equals(ProjectType.GRAPH)){
                paletteIFrame = new GraphPaletteIFrame();
                
            }
            jDesktopPaneMain.add(paletteIFrame);
            paletteIFrame.setVisible(true);
            ((IPalette)paletteIFrame).disableAllComponents();
            
            dataSelectIFrame = new DataSelectIFrame(this);
            jDesktopPaneMain.add(dataSelectIFrame);
            dataSelectIFrame.setVisible(true);
            
            isEditing = true;
            this.setTitle(KGlobal.APP_NAME+" - "+FunctionsHelper.getString("MainWindow.message.unsavedProject"));

            setDefaultPanelsLayout();
            
            App.setCurrentProjectType(type);
            
           
        }
    }
    
