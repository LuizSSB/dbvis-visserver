package br.unesp.amoraes.dbvis.beans;

/**
 * Database Connection Entity
 * Represents a configured connection with an external RDBMS
 * @author Alessandro Moraes (sanfatec at gmail.com)
 * @since 2013-01-04
 */
public class DatabaseConnectionEntity {
    private Integer id;
    private String name;
    private String driver;
    private String url;
    private String username;
    private String password;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    public String getDriver(){
        return driver;
    }
    
    public void setDriver(String driver){
        this.driver = driver;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 37 * hash + (this.id != null ? this.id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final DatabaseConnectionEntity other = (DatabaseConnectionEntity) obj;
        if (this.id != other.id && (this.id == null || !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }


    
    
    
}
